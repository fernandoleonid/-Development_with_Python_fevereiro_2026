frutas = ['uva', 'banana','morango','limão']

# Adiciona um  elemento no final da lista
frutas.append('Melão')
frutas.append('Melancia')
print (frutas)

# Excluir o último elemento
frutas.pop()
print (frutas)

# Excluir pelo indice 
frutas.pop(2)
print (frutas)

# Modifica um elemento
frutas[0] = 'UVA'
print (frutas)
